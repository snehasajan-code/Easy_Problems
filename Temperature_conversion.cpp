#include<iostream>
using namespace std;
int main()
{
    /*declaring two variables */
    float celsius,fahrenheit;

    /*input degree in celsius*/
    cout<<"Enter the celsius: ";
    cin>>celsius;

    /*applying conversion of degree celsius into fahrenheit formula */
    fahrenheit=(celsius*9/5)+32;

    /*printing the fahrenheit*/
    cout<<"Degree in fahrenheit: "<<fahrenheit;
    return 0;




}
